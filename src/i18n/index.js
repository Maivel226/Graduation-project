export function getSavedLanguage() {
  const saved = window.localStorage.getItem("teamup_language");
  return supportedLanguages[saved] ? saved : "en";
}

export function applyLanguageSettings(language) {
  const langConfig = supportedLanguages[language] || supportedLanguages.en;

  document.documentElement.lang = language;
  document.documentElement.dir = langConfig.dir;

  window.localStorage.setItem("teamup_language", language);
}

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: getSavedLanguage(),
    fallbackLng: "en",
    interpolation: {
      escapeValue: false
    }
  });

applyLanguageSettings(i18n.language);

